// Helper functions for curriculum data access
export const CurriculumHelpers = {
  getLearningAreas() {
    return [...new Set(CURRICULUM_DATABASE.learningAreas.map(item => item.learningArea))]
      .filter(Boolean)
      .sort();
  },

  getSubjectsByArea(learningArea) {
    const area = CURRICULUM_DATABASE.learningAreas.find(item => item.learningArea === learningArea);
    return area ? area.subjects : [];
  },

  getYearLevels() {
    return [
      "Foundation",
      "Year 1", "Year 2", "Year 3", "Year 4", "Year 5",
      "Year 6", "Year 7", "Year 8", "Year 9", "Year 10"
    ];
  },

  getAchievementStandardsBySelection(learningArea, subject, yearLevel) {
    if (!learningArea || !subject || !yearLevel) return [];
    
    return CURRICULUM_DATABASE.achievementStandards
      .filter(standard => 
        standard.learningArea === learningArea &&
        standard.subject === subject &&
        standard.yearLevel === yearLevel
      )
      .map((standard, index) => ({
        id: `${learningArea}-${subject}-${yearLevel}-${index}`,
        ...standard
      }));
  },

  getContentDescriptorsBySelection(learningArea, subject, yearLevel) {
    if (!learningArea || !subject || !yearLevel) return [];
    
    return CURRICULUM_DATABASE.contentDescriptors
      .filter(descriptor => 
        descriptor.learningArea === learningArea &&
        descriptor.subject === subject &&
        descriptor.yearLevel === yearLevel
      )
      .map((descriptor, index) => ({
        id: `${learningArea}-${subject}-${yearLevel}-${index}`,
        ...descriptor
      }));
  },

  getCurriculumContent(learningArea, subject, yearLevel) {
    return CURRICULUM_DATABASE.contentDescriptors.filter(item => 
      item.learningArea === learningArea && 
      item.subject === subject && 
      item.yearLevel === yearLevel
    );
  },

  getAchievementStandard(learningArea, subject, yearLevel) {
    return CURRICULUM_DATABASE.achievementStandards.find(item =>
      item.learningArea === learningArea &&
      item.subject === subject && 
      item.yearLevel === yearLevel
    );
  }
};

